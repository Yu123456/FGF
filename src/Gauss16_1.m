function y=Gauss16_1(a,b,x0)

% 本程序编写于2014年8月9日,硕士论文
% 计算16点Gauss-Legendre数值积分
% a,b:积分区间上下限
% x0  被积函数里的x

% 高斯点
Xcoe=[0.095012509837637, -0.095012509837637, 0.281603550779259, -0.281603550779259,...
    0.458016777657227, -0.458016777657227, 0.617876244402644, -0.617876244402644,...
    0.755404408355003, -0.755404408355003, 0.865631202387832, -0.865631202387832,...
    0.944575023073233, -0.944575023073233, 0.989400934991650, -0.989400934991650];
% 系数
Acoe=[0.189450610455069, 0.189450610455069, 0.182603415044924, 0.182603415044924,...
    0.169156519395003, 0.169156519395003, 0.149595988816577, 0.149595988816577,...
    0.124628971255534, 0.124628971255534, 0.095158511682493, 0.095158511682493,...
    0.062253523938648, 0.062253523938648, 0.027152459411754, 0.027152459411754];

a1=(b-a)/2;
x=Xcoe*a1+(a+b)/2;
y=sum(ff(x,x0).*Acoe)*a1;


end

% 积分被积函数
function y=ff(x,x0)

sx=sqrt(x.^2-x0^2);
y=exp(sx)./sx;

end